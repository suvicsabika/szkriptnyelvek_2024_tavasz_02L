#!/usr/bin/env python3

def main():
    for i in range(5):
        print(i, end=" ")
    print()
    
    for i in range(1, 5):
        print(i, end=" ")
    print()
    
    for i in range(1, 5, 2):
        print(i, end=" ")
    print()
    
    for i in range(15, 0, -1):
        print(i, end=" ")
    print()
    
if __name__ == "__main__":
    main()